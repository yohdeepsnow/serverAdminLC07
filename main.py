def penambahan(a,b):
	total = a+b
	return total

def pengurangan(a,b):
	total = a-b
	return total

def pengalian(a,b):
	total = a*b
	return total

def main():
	print(penambahan(10,5))
	print(pengurangan(10,5))
	print(pengalian(10,5))
main()
